namespace AppserverIo\Psr\EnterpriseBeans\Description;

/**
 * Abstract class for all method invocation descriptors.
 *
 * @author    Tim Wagner <tw@appserver.io>
 * @copyright 2018 TechDivision GmbH <info@appserver.io>
 * @license   http://opensource.org/licenses/osl-3.0.php Open Software License (OSL 3.0)
 * @link      https://github.com/appserver-io-psr/epb
 * @link      http://www.appserver.io
 */
interface MethodInvocationDescriptorInterface extends NameAwareDescriptorInterface
{

    /**
     * Return's the method name.
     *
     * @return string The method name
     */
    public function getMethodName();

    /**
     * Return's the method arguments.
     *
     * @return array The method arguments
     */
    public function getArguments();
}
